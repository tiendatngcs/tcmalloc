struct bench_profile {
    // The units here actually don't matter so long as they're consistent
    // between Entries in a given Empirical.
    size_t size;
    // Rate at which calls to malloc(<size>) are made
    double alloc_rate;
    // Number (at steady state) of extant <size>-sized objects
    double num_live;
};

// utility function to find ceiling of r in arr[l..h]
int findCeil(int prefix[], int r, int l, int h) {
    int mid;
    while (l < h)
    {
        mid = l + ((h - l) >> 1); // Same as mid = (l+h)/2
        (r > prefix[mid]) ? (l = mid + 1) : (h = mid);
    }
    return (prefix[l] >= r) ? l : -1;
}
 
// the main function that returns a random number
// from arr[] according to distribution array
// defined by freq[]. n is size of arrays.
int myRand(std::vector<size_t> arr, std::vector<int> freq, int n) {
    // Create and fill prefix array
    int prefix[n];
    int i;
    prefix[0] = freq[0];
    for (i = 1; i < n; ++i)
        prefix[i] = prefix[i - 1] + freq[i];
 
    // prefix[n-1] is sum of all frequencies.
    // Generate a random number with value from 1 to this sum
    // double r = std::fmod(rand(), prefix[n - 1]) + 1;
    int r = (rand() % prefix[n - 1]) + 1;
 
    // Find index of ceiling of r in prefix array
    int indexc = findCeil(prefix, r, 0, n - 1);

    return arr[indexc];
}

// get the size and frequency from a profile
void getStats(std::vector<bench_profile> profile, 
              std::vector<size_t> &sizeVec, 
              std::vector<int> &freqVec,
              std::map<size_t, int> &numLivesMap) {
    int s = profile.size();
    for(int i = 0; i < s; i++) {
        sizeVec.push_back(std::ceil(profile[i].size));
        freqVec.push_back(std::ceil(profile[i].alloc_rate));
        numLivesMap[profile[i].size] += std::ceil(profile[i].num_live);
    }
}

